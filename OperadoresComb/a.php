<?php
# OPERADORES DE ATRIBUIÇÃO COMBINADOS
# permite alterar o valor de uma variavel, a partir do seu valor original

$a = 10;
$a = $a + 20;  # 30
$a = $a - 3;   # 7
$a = $a * 2;   # 20
$a = $a / 2;   # 5

// simplificando
$a += 20;      # 30
$a -= 3;       # 7
$a *= 2;       # 20
$a /= 2;       # 5